"""Phase 5.7 — generate the tidy result tables (LaTeX) into article/tables/.

One table per claim, paired-subset N stated, SemEval per split. Recomputes from the run
stores via the §5.0 conventions (clamp / normalise / K=5 / pairing). NOT hand-edited.

  python -m experiments.analysis.make_phase5_tables
"""

from __future__ import annotations

import numpy as np
import pandas as pd
from sklearn.metrics import cohen_kappa_score

from experiments.analysis import phase5

K = 5
SEED = 20260610          # every bootstrap call gets a FRESH generator with this seed (2026-06-11):
TABLES = phase5.REPO_ROOT / "article" / "tables"
# call-order independence -- inserting a new table/contrast no longer shifts the CI digits of
# the others (the old module-level rolling RNG made every CI depend on its position in main()).


def _qwk_items(d):
    s = d[d.pred_norm.notna() & d.gold_norm.notna()].groupby("item_id").agg(p=("pred_norm", "mean"), g=("gold_norm", "mean"))
    if len(s) < 8:
        return np.nan, s
    po = (s.p * K).round().clip(0, K).astype(int)
    go = (s.g * K).round().clip(0, K).astype(int)
    return cohen_kappa_score(go, po, weights="quadratic", labels=list(range(K + 1))), s


def _boot_ci(sA, sB, nb=800):
    """Bootstrap dQWK = QWK(B)-QWK(A) over common items (item means already computed)."""
    items = sorted(set(sA.index) & set(sB.index))
    if len(items) < 8:
        return (np.nan, np.nan, np.nan)
    rng = np.random.default_rng(SEED)
    a = sA.loc[items]; b = sB.loc[items]
    pa, ga, pb = a.p.to_numpy(), a.g.to_numpy(), b.p.to_numpy()
    def q(p, g):
        return cohen_kappa_score((g * K).round().clip(0, K).astype(int),
                                 (p * K).round().clip(0, K).astype(int),
                                 weights="quadratic", labels=list(range(K + 1)))
    ds = [q(pb[i], ga[i]) - q(pa[i], ga[i]) for i in (rng.integers(0, len(items), len(items)) for _ in range(nb))]
    return tuple(np.percentile(ds, [2.5, 50, 97.5]))


def _tex(df, path, caption, label):
    cols = list(df.columns)
    head = " & ".join(str(c) for c in cols) + " \\\\"
    lines = [" & ".join(str(v) for v in r) + " \\\\" for r in df.itertuples(index=False)]
    body = ("\\begin{tabular}{" + "l" * len(cols) + "}\n\\hline\n" + head
            + "\n\\hline\n" + "\n".join(lines) + "\n\\hline\n\\end{tabular}")
    (TABLES / path).write_text(
        f"% auto-generated by make_phase5_tables.py -- do not hand-edit\n"
        f"\\begin{{table}}[t]\\centering\\small\n\\caption{{{caption}}}\\label{{{label}}}\n"
        + body + "\n\\end{table}\n")
    print(f"  wrote {path} ({len(df)} rows)")


def main() -> int:
    TABLES.mkdir(parents=True, exist_ok=True)
    df = phase5.load_main()
    interv = set(pd.read_parquet(phase5.REPO_ROOT / "data" / "processed" / "_ptcs_strata.parquet")
                 .query("stratum=='intervened'").item_id)
    print("Phase 5.7 tables ->", TABLES)

    # --- T1: RQ1 reasoning contrast (clean-only dQWK + CI, cost premium, consistency).
    #     Hybrid PT-CS rule (§11 2026-06-11): PT-CS contrasts are full-N primary + a verified
    #     (intervened-stratum) robustness column; '--' for the public datasets. ---
    rp = phase5.reasoning_paired(df)
    base = rp[(rp.context_level == "with_guidance") & (rp.scope == "question_by_question") & (rp.decomposition == "holistic")]
    rows = []
    for (ds, dom, m), g in base.groupby(["dataset", "domain", "model"]):
        o, n = g[g.reasoning == "off"], g[g.reasoning == "on"]
        if o.empty or n.empty:
            continue
        k = n.run_index.nunique(); ppi = n[n.parse_ok].groupby("item_id").size()
        clean = set(ppi[ppi == k].index) if n.parse_ok.mean() < 1 else set(n.item_id.unique())
        _, sA = _qwk_items(o[o.item_id.isin(clean)]); _, sB = _qwk_items(n[n.item_id.isin(clean)])
        lo, md, hi = _boot_ci(sA, sB)
        prem = pd.to_numeric(n.completion_tokens, errors="coerce").mean() / max(pd.to_numeric(o.completion_tokens, errors="coerce").mean(), 1)
        ver_cell = "--"
        if ds == "ptcs":
            cleanv = clean & interv
            _, sAv = _qwk_items(o[o.item_id.isin(cleanv)]); _, sBv = _qwk_items(n[n.item_id.isin(cleanv)])
            lov, mdv, hiv = _boot_ci(sAv, sBv)
            ver_cell = f"{mdv:+.3f} [{lov:+.2f},{hiv:+.2f}] ({len(cleanv)})" if mdv == mdv else f"-- (N={len(cleanv)}<8)"
        rows.append({"Dataset": ds, "Dom": dom[:4], "Model": m, "N": len(clean),
                     "dQWK": f"{md:+.3f}", "95\\% CI": f"[{lo:+.2f},{hi:+.2f}]",
                     "Sig": "yes" if (lo > 0 or hi < 0) else "no",
                     "dQWK-V [CI] (N$_V$)": ver_cell,
                     "Tok$\\times$": f"{prem:.0f}",
                     "SDk off$\\to$on": f"{o.groupby('item_id').pred_norm.std().mean():.3f}$\\to${n.groupby('item_id').pred_norm.std().mean():.3f}"})
    _tex(pd.DataFrame(rows), "tab_rq1_reasoning.tex",
         "RQ1: reasoning OFF$\\to$ON on the paired, clean (non-truncated) items -- agreement gain (dQWK, K=5), "
         "token cost premium, and within-item SD (consistency). PT-CS rows: full-N primary; the V column repeats "
         "the contrast on the PT-CS-verified (intervened) stratum as robustness (hybrid rule, 2026-06-11).", "tab:rq1")

    # --- T2: dimension contrasts (context per dataset, scope, decomposition).
    #     Hybrid PT-CS rule (§11 2026-06-11): PT-CS contrasts get a verified robustness column. ---
    def contrast_row(A, B, label, nb=800, verified=None):
        _, sA = _qwk_items(A); _, sB = _qwk_items(B)
        if isinstance(sA, float) or isinstance(sB, float):
            return None
        lo, md, hi = _boot_ci(sA, sB, nb)
        n = len(set(sA.index) & set(sB.index))
        ver_cell = "--"
        if verified is not None:
            _, sAv = _qwk_items(A[A.item_id.isin(verified)]); _, sBv = _qwk_items(B[B.item_id.isin(verified)])
            lov, mdv, hiv = _boot_ci(sAv, sBv, nb)
            nv = len(set(sAv.index) & set(sBv.index))
            ver_cell = f"{mdv:+.3f} [{lov:+.2f},{hiv:+.2f}] ({nv})" if mdv == mdv else f"-- (N={nv}<8)"
        return {"Contrast": label, "N": n, "dQWK": f"{md:+.3f}", "95\\% CI": f"[{lo:+.2f},{hi:+.2f}]",
                "Sig": "yes" if (lo > 0 or hi < 0) else "no", "dQWK-V [CI] (N$_V$)": ver_cell}
    q = df[(df.model == "qwen3.5") & (df.reasoning == "off")]   # decomposition kept (needed below)
    we = set(df[df.scope == "whole_exam"].item_id)
    drows = []
    for ds, dom, gt in [("mohler", "short_answer", "ref"), ("semeval", "short_answer", "ref"),
                        ("riayn", "code", "rubric"), ("ptcs", "code", "rubric")]:
        c = q[(q.dataset == ds) & (q.domain == dom) & (q.scope == "question_by_question") & (q.decomposition == "holistic")]
        r = contrast_row(c[c.context_level == "none"], c[c.context_level == "with_guidance"],
                         f"context[{gt}] {ds}:{dom[:4]} none$\\to$guid",
                         verified=interv if ds == "ptcs" else None)
        if r: drows.append(r)
    sc = q[(q.dataset == "ptcs") & (q.domain == "code") & (q.context_level == "with_guidance") & (q.decomposition == "holistic")]
    drows.append(contrast_row(sc[(sc.scope == "question_by_question") & sc.item_id.isin(we)],
                              df[df.scope == "whole_exam"], "scope ptcs:code qbq$\\to$whole-exam",
                              verified=interv))
    dd = q[(q.dataset == "ptcs") & (q.domain == "code") & (q.context_level == "with_guidance") & (q.scope == "question_by_question")]
    drows.append(contrast_row(dd[dd.decomposition == "holistic"], dd[dd.decomposition == "criterion"],
                              "decomp ptcs:code holistic$\\to$criterion", verified=interv))
    _tex(pd.DataFrame([r for r in drows if r]), "tab_dimension_contrasts.tex",
         "RQ2/RQ3: context (per dataset; grounding type differs; the context arm ran on Qwen3.5 only), scope, "
         "and decomposition contrasts (dQWK, K=5, bootstrap CI). Qwen3.5, reasoning off. PT-CS rows: full-N "
         "primary; the V column repeats the contrast on PT-CS-verified (hybrid rule, 2026-06-11).", "tab:dims")

    # --- T3: SemEval per split ---
    ss = phase5.semeval_splits(df)
    ss = ss[ss.reasoning == "off"].drop(columns=["reasoning"]).rename(columns={"macroF1": "macroF1"})
    _tex(ss.round(3), "tab_semeval_splits.tex",
         "SemEval per split (fixed-0.5 accuracy + macro-F1; threshold-free AUROC/Spearman). Reasoning off, "
         "with-guidance cell only (all models like-for-like); n = run-rows (k repetitions pooled).", "tab:semeval")

    # --- T4: PT-CS reliability strata ---
    st = pd.read_parquet(phase5.REPO_ROOT / "data" / "processed" / "_ptcs_strata.parquet")
    sc = (st.stratum.value_counts().rename("n").to_frame())
    sc["pct"] = (100 * sc.n / sc.n.sum()).round(1)
    lab = {"intervened": "intervened (review evidence)", "exact_sum": "exact-sum (suspected unreviewed)",
           "no_criteria": "no-criteria (unassessable)"}
    sc = sc.reset_index()
    sc["stratum"] = sc["stratum"].map(lambda s: lab.get(s, s))
    sc = sc.rename(columns={"stratum": "Stratum"})
    _tex(sc, "tab_ptcs_strata.tex",
         "PT-CS gold reliability strata (intervention evidence: final grade vs sum of per-criterion scores). "
         "Only the intervened stratum has evidence of human review.", "tab:strata")

    # --- T4b: TRANSFER on PT-CS-verified short-answer (open models headline; anchor N=11 corroboration) ---
    sa = df[(df.domain == "short_answer") & (df.dataset == "ptcs") & (df.context_level == "with_guidance")
            & (df.scope == "question_by_question") & (df.decomposition == "holistic") & df.item_id.isin(interv)]
    trows = []
    for (m, r), g in sa.groupby(["model", "reasoning"]):
        q, s = _qwk_items(g)
        if isinstance(q, float) and np.isnan(q):
            n = len(g.item_id.unique()); trows.append({"Config": f"{m}|{r}", "QWK": "--", "N": n, "95\\% CI": "(N<8)", "Note": "anchor" if m == "gpt-5.1" else ""})
            continue
        idx = s.index.to_numpy()
        rng = np.random.default_rng(20260611)        # same draw scheme as tab_ranking_transfer's _ci_local
        boot = [cohen_kappa_score((s.loc[x].g * K).round().clip(0, K).astype(int), (s.loc[x].p * K).round().clip(0, K).astype(int),
                weights="quadratic", labels=list(range(K + 1))) for x in (rng.choice(idx, len(idx), True) for _ in range(800))]
        lo, hi = np.percentile(boot, [2.5, 97.5])
        trows.append({"Config": f"{m}|{r}", "QWK": f"{q:.3f}", "N": len(s), "95\\% CI": f"[{lo:.2f},{hi:.2f}]",
                      "Note": "anchor (N=11, corrob. only)" if m == "gpt-5.1" else ""})
    td = pd.DataFrame(trows)
    td["_a"] = td.Config.str.startswith("gpt-5.1"); td["_q"] = pd.to_numeric(td.QWK, errors="coerce")
    td = td.sort_values(["_a", "_q"], ascending=[True, False]).drop(columns=["_a", "_q"])  # open models lead; anchor last
    _tex(td, "tab_transfer_verified.tex",
         "RQ6 transfer on PT-CS-verified short-answer (intervened stratum). Headline via the open models "
         "(N=84 OFF / 32 ON); the GPT-5.1 anchor (N=11) is directional corroboration only -- its CI self-defends.",
         "tab:transfer")

    # --- T4d: RQ6 ranking comparison -- public vs PT-CS-verified, declared basis (added 2026-06-11;
    #     replaces the unreproducible 'ranking inversion / public winner qwen|off' prose claim).
    #     Uses a LOCAL rng for the small-N CIs so the shared RNG stream (other tables) is untouched. ---
    base_all = df[(df.context_level == "with_guidance") & (df.scope == "question_by_question")
                  & (df.decomposition == "holistic")]

    def _q(d):
        q, s = _qwk_items(d)
        return (q if not (isinstance(q, float) and np.isnan(q)) else np.nan), len(s)

    def _ci_local(d, nb=800):
        s = d[d.pred_norm.notna() & d.gold_norm.notna()].groupby("item_id").agg(p=("pred_norm", "mean"), g=("gold_norm", "mean"))
        rng = np.random.default_rng(20260611)
        idx = s.index.to_numpy()
        boot = [cohen_kappa_score((s.loc[x].g * K).round().clip(0, K).astype(int),
                                  (s.loc[x].p * K).round().clip(0, K).astype(int),
                                  weights="quadratic", labels=list(range(K + 1)))
                for x in (rng.choice(idx, len(idx), True) for _ in range(nb))]
        lo, hi = np.percentile(boot, [2.5, 97.5])
        return lo, hi

    rk = []
    for m in ["qwen3.5", "glm-5.1", "deepseek-v4-flash", "gpt-5.1"]:
        for r in ["off", "on"]:
            g = base_all[(base_all.model == m) & (base_all.reasoning == r)]
            per = {ds: _q(g[g.dataset == ds])[0] for ds in ["mohler", "semeval", "riayn"]}
            pub = float(np.nanmean(list(per.values())))
            cells = {}
            for dom, key in [("short_answer", "sa"), ("code", "code")]:
                v = g[(g.dataset == "ptcs") & (g.domain == dom) & g.item_id.isin(interv)]
                q, nv = _q(v)
                if q == q and nv < 100:                      # CI beside every small-N number
                    lo, hi = _ci_local(v)
                    cells[key] = (q, nv, f"{q:.3f} [{lo:.2f},{hi:.2f}]")
                elif q == q:
                    cells[key] = (q, nv, f"{q:.3f}")
                else:
                    cells[key] = (np.nan, nv, "--")
            rk.append({"m": m, "r": r, "per": per, "pub": pub, "sa": cells["sa"], "code": cells["code"]})

    def _rank(vals):
        open_v = sorted([v for v in vals if v == v], reverse=True)
        return {v: i + 1 for i, v in enumerate(open_v)}
    pub_rank = _rank([x["pub"] for x in rk if x["m"] != "gpt-5.1"])
    sa_rank = _rank([x["sa"][0] for x in rk if x["m"] != "gpt-5.1"])
    code_rank = _rank([x["code"][0] for x in rk if x["m"] != "gpt-5.1"])
    rkrows = []
    for x in sorted(rk, key=lambda x: (x["m"] == "gpt-5.1", -x["pub"])):
        anchor = x["m"] == "gpt-5.1"
        rkrows.append({"Config": f"{x['m']}|{x['r']}",
                       "Public QWK (rk)": f"{x['pub']:.3f}" + ("" if anchor else f" ({pub_rank[x['pub']]})"),
                       "V-short QWK [CI] (N, rk)": x["sa"][2] + f" ({x['sa'][1]}" + ("" if anchor else f", {sa_rank[x['sa'][0]]}") + ")",
                       "V-code QWK [CI] (N, rk)": x["code"][2] + f" ({x['code'][1]}" + ("" if anchor else f", {code_rank[x['code'][0]]}") + ")",
                       "Note": "anchor -- corrob. only, unranked" if anchor else ""})
    winners = {ds: max(((x["per"][ds], f"{x['m']}|{x['r']}") for x in rk if x["m"] != "gpt-5.1")) for ds in ["mohler", "semeval", "riayn"]}
    wtxt = "; ".join(f"{ds}: {w[1]} ({w[0]:.3f})" for ds, w in winners.items())
    _tex(pd.DataFrame(rkrows), "tab_ranking_transfer.tex",
         "RQ6 ranking comparison, public vs PT-CS-verified. Basis: QWK (K=5) on item means, with-guidance, "
         "question-by-question, holistic cells; public = unweighted mean over Mohler/SemEval/RIAYN at full N "
         "(open models k=5; the GPT-5.1 anchor graded 60-item subsets -- reference only, unranked). Ranks over "
         f"the open configs. Per-dataset public winners differ -- {wtxt}. The public top "
         "(qwen3.5$|$on $\\approx$ glm-5.1$|$on) contains the verified short-answer winner: the top transfers; "
         "the mid-ranking does not.", "tab:ranktransfer")

    # --- T4c: gold full-vs-verified SENSITIVITY (the 'unreliable gold distorts' result) ---
    def qcell(d):
        q, s = _qwk_items(d)
        return (f"{q:.3f}" if not (isinstance(q, float) and np.isnan(q)) else "--"), (len(s) if not isinstance(s, float) else 0)
    qcode = df[(df.dataset == "ptcs") & (df.domain == "code") & (df.model == "qwen3.5") & (df.reasoning == "off")
               & (df.context_level == "with_guidance") & (df.scope == "question_by_question") & (df.decomposition == "holistic")]
    rub = df[(df.dataset == "ptcs") & (df.domain == "code") & (df.model == "qwen3.5") & (df.reasoning == "off")
             & (df.scope == "question_by_question") & (df.decomposition == "holistic")]
    def dq(sub):
        _, a = _qwk_items(rub[(rub.context_level == "none") & sub]); _, b = _qwk_items(rub[(rub.context_level == "with_guidance") & sub])
        lo, md, hi = _boot_ci(a, b)
        return f"{md:+.3f} [{lo:+.2f},{hi:+.2f}]"
    sens = pd.DataFrame([
        {"Metric": "Qwen OFF code QWK", "Full PT-CS": qcell(qcode)[0], "PT-CS-verified": qcell(qcode[qcode.item_id.isin(interv)])[0]},
        {"Metric": "rubric dQWK (Qwen OFF, none$\\to$guid)", "Full PT-CS": dq(rub.item_id.notna()), "PT-CS-verified": dq(rub.item_id.isin(interv))},
        {"Metric": "model$-$gold deviation (OFF)",
         "Full PT-CS": f"{phase5.signed_deviation(qcode):+.3f}",
         "PT-CS-verified": f"{phase5.signed_deviation(qcode[qcode.item_id.isin(interv)]):+.3f}"},
    ])
    _tex(sens, "tab_gold_sensitivity.tex",
         "Sensitivity: full PT-CS vs PT-CS-verified. Non-validated gold distorts the evaluation (QWK) and masks "
         "real effects (the rubric benefit), and artificially shrinks the model$-$gold deviation. This is itself a result.",
         "tab:goldsens")

    # --- T5: framework decision guide (the deliverable) ---
    fw = pd.DataFrame([
        {"Task / context": "Short-answer + reference answer", "Recommended": "OFF, discriminating model",
         "Trade-off in the rule": "reasoning gain not significant; OFF 10--925$\\times$ cheaper (model-dependent)"},
        {"Task / context": "Code, model that DISCRIMINATES (GLM/DeepSeek/GPT)", "Recommended": "Reasoning OFF",
         "Trade-off in the rule": "ON not significant + 116--192$\\times$ tokens $\\to$ keep OFF"},
        {"Task / context": "Code, model that COLLAPSES (Qwen)", "Recommended": "ON, or switch model",
         "Trade-off in the rule": "recovers QWK on tractable items at $\\sim$800$\\times$ tokens + 2.5--5$\\times$ worse SDk; prefer switching"},
        {"Task / context": "Consistency/fairness-critical", "Recommended": "Reasoning OFF",
         "Trade-off in the rule": "ON less reproducible (partly length$\\times$backend, partly reasoning-intrinsic)"},
        {"Task / context": "Multi-question / session", "Recommended": "Clean, question-by-question",
         "Trade-off in the rule": "shared history $\\to$ stricter (p$<$.01); order adds variance"},
        {"Task / context": "Deploy in a new context", "Recommended": "Validate locally (governs the above)",
         "Trade-off in the rule": "transfer is partial: top config carried over, mid-ranking shuffled; unvalidated gold flipped effect readings"},
    ])
    _tex(fw, "tab_framework.tex",
         "The configuration decision guide (Phase 5.6): starting priors per axis, with the trade-off inside "
         "each rule; ``validate locally'' governs the per-axis priors.", "tab:framework")
    print("done.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

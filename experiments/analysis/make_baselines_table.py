"""Generate the published-performance CONTEXT table (Phase 1.5).

Reads experiments/analysis/published_baselines.csv (hand-curated, source-verified
literature numbers) and emits article/tables/published_baselines.tex (IEEEtran table*).

CRITICAL FRAMING (CLAUDE.md §6.3): these numbers were produced under DIFFERENT models,
prompts, and protocols. They SITUATE our work as external context; they are NOT the
success criterion. Validation comes from our own controlled comparisons (Phase 4-5).

Run:  python -m experiments.analysis.make_baselines_table
"""

from __future__ import annotations

from pathlib import Path

import pandas as pd

REPO_ROOT = Path(__file__).resolve().parents[2]
CSV = REPO_ROOT / "experiments" / "analysis" / "published_baselines.csv"
OUT = REPO_ROOT / "article" / "tables" / "published_baselines.tex"

GROUP_ORDER = [
    ("OUR-shortanswer", "Our short-answer datasets (Mohler, SemEval-2013 T7: SciEntsBank/Beetle)"),
    ("OUR-code", "Our code datasets (RIAYN OOP/DSA) and nearest code-rubric analogues"),
    ("DEMARCATION", "Model-comparison-as-graders (demarcation: we do a framework, not a ranking)"),
    ("BENCHMARK-context", "Benchmark context (ASAP/ASAP-SAS, not our datasets)"),
    ("LLM-comparison", "LLM-as-grader landscape (mixed datasets: which models, which metrics)"),
]
PROV = {"PDF": r"$\bullet$", "SLR": r"$\circ$", "ABS": r"$\dagger$"}


def esc(s: str) -> str:
    # Order matters: structural escapes (\ & % _ # $ { }) run FIRST, so the math snippets
    # introduced afterwards ($\sim$, $>$, $\gg$, $\pi$ ...) keep their literal $ and \.
    # In this numeric context every raw '~' means "approximately" (-> $\sim$, not a text
    # tilde), and '>>' / '>' are comparison operators; '>>' must precede the single '>'.
    s = str(s)
    for a, b in [("\\", r"\textbackslash{}"), ("&", r"\&"), ("%", r"\%"), ("_", r"\_"),
                 ("#", r"\#"), ("$", r"\$"), ("{", r"\{"), ("}", r"\}"),
                 ("~", r"$\sim$"), ("^", r"\textasciicircum{}"),
                 (">>", r"$\gg$"), (">", r"$>$"), ("+-1", r"$\pm$1"),
                 ("kappa", r"$\kappa$"), ("pi extractable", r"$\pi$ extractable")]:
        s = s.replace(a, b)
    return s


def main() -> int:
    # COMPACT rendering (user decision, review pass): the article shows only the rows flagged
    # compact=1 in the CSV (one representative result per dataset + the demarcation and landscape
    # anchors). The full 27-row curation stays in the CSV as the data record; the Protocol and
    # provenance columns are dropped from the article (the citation is the provenance).
    df = pd.read_csv(CSV).fillna("")
    full_n = len(df)
    if "compact" in df.columns:
        df = df[df["compact"] == 1]
    lines: list[str] = []
    A = lines.append
    A(r"% AUTO-GENERATED by experiments/analysis/make_baselines_table.py -- do not hand-edit.")
    A(r"\begin{table*}[!tp]")
    A(r"\centering")
    A(r"\caption{Representative published results on our datasets and adjacent benchmarks, as "
      r"external context. These numbers were produced under \emph{different models, prompts, and "
      r"protocols} (supervised 2019--2025 or prompted LLMs); they situate our study and are "
      r"\textbf{not} its success criterion, which comes from our own controlled comparisons.}")
    A(r"\label{tab:published-baselines}")
    A(r"\footnotesize")
    A(r"\setlength{\tabcolsep}{4pt}")
    A(r"\renewcommand{\arraystretch}{1.12}")
    A(r"\begin{tabular}{>{\raggedright\arraybackslash}p{3.0cm} "
      r">{\raggedright\arraybackslash}p{3.2cm} >{\raggedright\arraybackslash}p{3.2cm} "
      r">{\raggedright\arraybackslash}p{2.8cm} >{\raggedright\arraybackslash}p{4.6cm}}")
    A(r"\hline")
    A(r"\textbf{Dataset} & \textbf{Study (year)} & \textbf{Model / method} & "
      r"\textbf{Metric} & \textbf{Value / note}\\")
    A(r"\hline")
    for gkey, _ in GROUP_ORDER:           # group ORDER kept; header rows dropped (table is short)
        for _, r in df[df.group == gkey].iterrows():
            # study cell carries a resolvable \cite (do NOT escape the cite command)
            study = esc(f"{r.study} ({r.year})") + (rf"~\cite{{{r.ref}}}" if r.ref else "")
            # §9.2: no en-dash as prose punctuation in table notes. Join value and note with a
            # colon; when value is the "--" placeholder (no numeric metric), show the note alone.
            if not r.note:
                val = r.value
            elif str(r.value).strip() in ("", "--"):
                val = r.note
            else:
                val = f"{r.value}: {r.note}"
            A(" & ".join([esc(r.dataset), study, esc(r.model_method),
                          esc(r.metric), esc(val)]) + r"\\")
    A(r"\hline")
    A(r"\end{tabular}")
    A(r"\end{table*}")
    OUT.parent.mkdir(parents=True, exist_ok=True)
    OUT.write_text("\n".join(lines) + "\n", encoding="utf-8")

    print(f"Wrote {OUT.relative_to(REPO_ROOT)}  ({len(df)} compact rows of {full_n} curated)")
    print("\nCompact rows per group:")
    print(df.group.value_counts().reindex([g for g, _ in GROUP_ORDER]).fillna(0).astype(int).to_string())
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
